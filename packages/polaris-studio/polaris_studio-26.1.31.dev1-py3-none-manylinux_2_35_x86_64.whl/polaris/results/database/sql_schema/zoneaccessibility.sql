--@ ZoneAccessibility table stores the general and transit-based accessibility values for each TAZ Zone in the model, including average travel attributes (e.g. distance, time, cost) by mode and time periods.
--@ The accessibility variables refer to the access to number of employment of a particular type available with the zone as origin based on an exponential decay of auto travel time
--@ As there a large number of columns in this table a guide to interpreting their names is required. Columns are generally composites of attribute/mode/time - for instance 'avg_ampeak_pnr_ivtt_inbound' is the average in-vehicle travel time for park and ride trips during the morning peak.
--@
--@ The following dimensions are commonly used:
--@
--@ time_period:
--@ 
--@ * 06:00 - 09:00 (AM peak), 
--@ * 09:00 - 16:00 (off peak),  
--@ * 16:00 - 19:00 (PM peak), 
--@ * 19:00 - 06:00 (night).
--@
--@ mode: 
--@
--@ * auto (SOV - single-occupancy vehicle),
--@ * park-and-ride (pnr), 
--@ * park-and-rail (pnrail), 
--@ * rail, 
--@ * transit,
--@ * walk,
--@ * bike.
--@
--@ attributes (in general, `avg_` refers to AVERAGE): 
--@
--@ * accessibility - access to number of employment of particular type available with the zone as origin based on an exponential decay of auto travel time
--@ * ivtt - in-vehicle travel time
--@ * ovtt - access/egress travel time
--@ * fare - fares of a mode
--@ * transfers - number of transfers
--@ * wait - waiting time
--@ * ttime - travel time
--@ * ttime_var - travel time variability
--@ * cost - travel cost of a mode
--@ * distance - distance traveled.
--@
--@ Additionally, many metrics are also duplicated with their '_inbound' version which describes the values only for the inbound direction of travel.
--@

