from hfortix_fortios._protocols import CRUDEndpoint

class Stp(CRUDEndpoint, MetadataMixin):
    """Stp Operations."""
    
    # Configure metadata mixin to use this endpoint's helper module
    _helper_module_name = "stp"
    
    # ========================================================================
