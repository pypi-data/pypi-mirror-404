"""Pytest move_years"""

from ArWikiCats.fix.mv_years import move_years


def load_test_move_years(data: dict) -> None:
    diff_list = []
    same = 0
    for title, expected in data.items():
        result = move_years(title)
        if result != expected:
            diff_list.append({"input": title, "actual": result, "expected": expected})
        else:
            assert result == expected
            same += 1
    len_diff_list = len(diff_list)
    print(f"<<green>> {len_diff_list=}, {same=}.")

    if len_diff_list:
        diff_details = "\n".join(str(entry) for entry in diff_list)
        raise AssertionError(f"Found {len_diff_list} transformation mismatches: {diff_details}")


