"""
类方法信息类
用于存储类方法的详细信息，提供更好的类型提示和代码补全
"""

from typing import List


class 类方法信息:
    """存储类方法信息的类"""
    
    def __init__(
        self,
        名称: str,
        返回类型: str,
        方法体: List[str],
        是构造函数: bool = False,
        是静态方法: bool = False,
        是属性: bool = False,
        是运算符: bool = False,
        参数列表: str = "",
    ):
        """
        初始化类方法信息
        
        Args:
            名称: 方法名
            返回类型: C++ 返回类型
            方法体: 方法体代码行列表
            是构造函数: 是否为构造函数
            是静态方法: 是否为静态方法
            是属性: 是否为属性
            是运算符: 是否为运算符重载
            参数列表: 参数列表字符串
        """
        self.名称 = 名称
        self.返回类型 = 返回类型
        self.方法体 = 方法体
        self.是构造函数 = 是构造函数
        self.是静态方法 = 是静态方法
        self.是属性 = 是属性
        self.是运算符 = 是运算符
        self.参数列表 = 参数列表
